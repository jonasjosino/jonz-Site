	<h3>10 - Envie suas conversas por e-mail</h3>
 
Em suas configurações, vá em “Configurações de conversa” e selecione a opção “Enviar conversa por email”. Você poderá escolher a conversa específica e o email para qual ela será enviada! Uma das melhores dicas para o Whatsapp para quem tem informações importantes em suas conversas.
 
 </br></br>
