<h3>12 - Faça o Whatsapp mostrar notificações pop-up</h3>
 
Para receber uma notificação pop-up (aqueles que aparecem na sua tela inicial) sempre que alguém te mandar uma mensagem no Whatsapp, é só ir nas configurações de conversa e selecionar “Notificações”.

Entre as opções, escolha “Notificação pop-up” e veja como prefere que o app avise que recebeu novas mensagens.
 
