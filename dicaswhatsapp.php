</br></br></br>

<h3> 2 - Esconder o “visto por último”</h3>

Para não deixar que seus contatos saibam a última vez que você usou o Whatsapp, vá em suas configurações e procure pela opção “Privacidade” em sua conta. Selecione o “Visto por último” e escolha a opção “Ninguém”.


</br></br>
<img src="">
</br></br>

</br></br></br>

<h3>3 - Esconder sua foto de perfil</h3>
 
Em “Privacidade”, vá até a opção “Foto do perfil” e veja se prefira que só seus contatos possam ver sua foto ou se ninguém poderá vê-la.
 
 </br></br>
<img src="">
</br></br></br>

<h3>4 - Esconder seu status</h3>

ara não deixar os outros verem seu status, volte ao menu “Privacidade” e marque “Ninguém” na opção “Status”.

Dessa forma, você poderá ter mais privacidade quanto os seus contatos!


</br></br></br>

<h3>5 - Deletar sua conta</h3>
 
Em configurações, escolha a opção “Conta” e selecione “Apagar minha conta” para deletar tudo. Aconselhamos que você pense bem, porque depois não há mais volta para recuperar o que perder.
 
 </br></br>
<img src="">
</br></br></br>

<h3>6 - Gerenciar o download automático de mídias</h3>
 
Uma situação bem chata é quando vídeos, arquivos de áudio ou fotos enviadas a você em suas conversas, são baixadas automaticamente no seu celular. Isso pode ser até perigoso dependendo do tipo de arquivo enviado.

Mas há um jeito bem simples de resolver isso: Vá em suas configurações, selecione “Configurações de conversa” e clique em “Download automático”. Lá você poderá escolher se quer continuar baixando arquivos automaticamente ou não.
 
 </br></br>
<img src="">
</br></br></br>

<h3>7 - Usar o Whatsapp no PC</h3>
 
Para não gastar toda a bateria do seu smartphone só falando no Whatsapp, é possível levar suas conversas para o PC. Basta abrir seu aplicativo normalmente, ir em suas configurações e clicar na opção “Whatsapp Web”.

O app pedirá que você acesse o site do Whatsapp Web (clique neste link para acessá-lo!), onde verá um código QR. Faça isso de um computador e posicione seu smartphone (que deve estar ainda no app e pronto para escanear o código) na frente da tela para ler o código QR.

A partir daí, sempre que você visitar esse endereço do Whatsapp Web neste computador, poderá conferir suas conversas. Só lembre de não fazer isso em um PC público ou pertencente a outra pessoa, senão é capaz de muita gente ver suas conversas.
 
 </br></br>
<img src="">
</br></br></br>

<h3>8 - Usar o Whatsapp sem um cartão SIM</h3>
 
Há quem não saiba, mas se seu Whatsapp já estiver configurado em um celular, você não precisará mais do seu cartão SIM (o seu chip de telefone). Ou seja, se ele quebrar ou algo do tipo, você poderá continuar usando o serviço do app sem problemas.

Caso queira configurar um novo dispositivo sem realmente usar o SIM, basta colocar o chip em outro celular para receber aquele SMS de verificação que o aplicativo sempre manda quando você o usa pela primeira vez.
 
 </br></br>
<img src="">
</br></br></br>

<h3> 9 - Desative o marcador azul de mensagem lida</h3>
 
Se não quiser que os outros saibam quando você leu uma mensagem, basta ir em suas configurações e voltar na opção “Privacidade”. A caixinha da “Confirmação de leitura” deverá estar preenchida por padrão, significando que seus contatos sabem quando você leu uma mensagem, já que aquele marcador azul aparece para eles.

Basta clicar nesta opção para desativar a função, mas se fizer isso, você também não saberá quando alguém leu suas mensagens.
 
 </br></br>
