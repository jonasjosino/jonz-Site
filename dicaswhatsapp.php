	<h2>Introdução</h2>

É quase impossível encontrar alguém com um smartphone que ainda não use o Whatsapp para conversar com seus amigos e família. Mas pouca gente sabe das dicas para o Whatsapp que podem melhorar ainda mais o seu uso.

Com alguns truques, você pode até personalizar sua experiência com o app, por isso, listamos nada menos do que 20 truques e dicas para o Whatsapp que podem ser bem úteis no seu dia a dia. Confira!
	
</br></br></br>

<h3>1 - Mudar seu número do Whatsapp</h3>

Caso você precise trocar de número de celular mas não quer perder suas conversas e contatos do Whatsapp, há uma maneira bem simples de mudar o telefone no próprio app. Vá em suas configurações e procure pela opção “Conta”. Lá, é só clicar em “Mudar o número” e seguir as instruções do aplicativo.



</br></br></br>
