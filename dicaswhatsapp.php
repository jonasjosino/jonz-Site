<h3>11 - Bloqueie um contato no aplicativo</h3>
 
sta é uma opção bem simples, útil e que muita gente já conhece, mas sempre é bom explicar para que ainda nunca usou. Para bloquear alguém, basta ir na conversa com esta pessoa e clicar na opção “Mais” nas configurações. Lá, é só clicar no “Bloquear” para realizar a tarefa.

Para desbloquear esta mesma pessoa, basta seguir o mesmo caminho e só clicar em “Desbloquear”. Bem fácil, não é?!
 
 </br></br>
<img src="">
