	<h2>Introdução</h2>
O computador faz parte do cotidiano de muitas pessoas, seja para fins profissionais, seja para estudos, seja para diversão, enfim. Como consequência, muita gente acaba passando horas e mais horas por dia na frente do PC, e esta situação pode, mais cedo ou mais tarde, resultar em problemas de saúde. Para ajudar a evitar estes males, o InfoWester apresenta a seguir 10 dicas simples, mas muito importantes sobre postura e outros aspectos a respeito do uso correto do computador.
</br></br></br>

<h3> 1 -   Sente-se corretamente na cadeira</h3>

Ao se sentar, mantenha suas costas retas, de forma que estas se apoiem no encosto da cadeira. Neste sentido, não use cadeiras com encosto curto e tome cuidado para não deixar este item incorretamente inclinado, quando for possível ajustá-lo.

Também evite deixar seus ombros caídos e não adiante suas pernas de forma que seus pés fiquem muito à frente da linha dos joelhos, como se você fosse se deitar - é importante deixar suas coxas quase que totalmente posicionadas no assento da cadeira.

