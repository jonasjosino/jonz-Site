<h3>4 - Mantenha o monitor em frente ao seu rosto</h3>

Mantenha o monitor de vídeo em uma posição frontal ao seu rosto, de forma que você não tenha que levantar/abaixar a cabeça ou girá-la para ver a tela do computador.

Se estiver utilizando notebook ou ultrabook, é uma boa ideia conectá-lo a um monitor com altura adequada, caso permaneça por um longo período diante do dispositivo, do contrário, você passará muito tempo inclinando seu corpo para frente para visualizar a tela satisfatoriamente.



<h3>5 - Fique a pelo menos 50 cm de distância da tela</h3>


Se você "grudar" a cara no monitor, seus olhos ficarão cansados mais rapidamente e você acabará forçando-os para enxergar, podendo ter dores de cabeça ou algum tipo de irritação. Por este motivo, mantenha uma distância de pelo menos 50 centímetros da tela do computador.

Também é uma boa ideia ajustar os níveis de contraste e brilho do monitor e usar modelos que ofereçam boa qualidade visual. Regule também a resolução do equipamento para um padrão confortável aos seus olhos - se as letras ou objetos exibidos na tela lhe parecerem muito pequenos, alterne entre as resoluções disponíveis até encontrar uma que lhe pareça confortável.


