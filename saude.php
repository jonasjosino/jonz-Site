<h3>3 -  Mantenha os cotovelos na mesma aultura dos pulsos </h3>
 
Dê preferência a cadeiras que tenham apoiadores para os braços, de forma que os cotovelos não fiquem abaixo da linha dos pulsos. Desta maneira, você conseguirá usar todo o braço para manipular o teclado e o mouse. Os pulsos também não podem ficar muito abaixo da linha dos dedos.
 </br></br>
