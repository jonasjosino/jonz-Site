<h3>7 - Pisque mais</h3>


Piscar mais?! Sim! Repare que quando você está muito concentrado ou dando bastante atenção a um assunto, os seus olhos ficam bem abertos diante da tela e, consequentemente, piscam menos. Se situações do tipo acontecerem com frequência, você poderá ter algum tipo de irritação nos olhos, como ardência e cansaço visual.

O problema pode ficar pior se você passar muito tempo em um ambiente com ar condicionado funcionando. Assim, ao sentir sinais de irritação nos olhos, comece a piscar mais. Você também pode fazer compressas com água fria e aplicar sobre os olhos por 3 minutos (com eles fechados, obviamente). Também há colírios que ajudam a lidar com este problema, mas é importante utilizá-los somente com prescrição médica.

</br></br></br>

</br></br>


<h3>8 - Use o computador em um ambiente bem iluminado</h3>


Ambientes mal iluminados podem causar vários problemas, como sonolência, sensação de confinamento (aumentando o estresse), perda de noção do horário, entre outros. Por isso, é importante usar o computador em um lugar bem iluminado, preferencialmente com luzes brancas distribuídas de maneira uniforme.

É válido ressaltar que a luz não deve focalizar o seu rosto e muito menos a tela do monitor, como acontece quando se usa o computador de costas para uma janela com forte incidência de luz solar, já que estas situações fazem com você não consiga enxergar corretamente o conteúdo da tela.

</br></br></br>


</br></br>


<h3>9 - Evite se alimentar diante do computador</h3>

Higiene: farelo de salgados, molhos de lanches, pedaços de biscoitos ou pães, entre outros, podem cair no teclado ou na mesa, podendo atrair insetos ou até mesmo causar mal cheiro;
Você precisa prestar atenção na comida: se você estiver fazendo outra atividade enquanto come, acaba não prestando a devida atenção na comida. Esta situação pode fazer com que você mastigue rapidamente o alimento ou coma demais sem perceber, comportamento capaz de causar indigestão e outros problemas.

