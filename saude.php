<h3>6 - Levante-se a pelo menos cada 50 minutos</h3>

Não é bom permanecer muito tempo mesma na posição. O corpo humano foi preparado para se mover regularmente, por isso, levanta-se a cada 50 minutos, pelo menos. Se você estiver em um escritório, por exemplo, vá ao banheiro ou caminhe até o bebedouro do seu andar. Se o telefone tocar em outra mesa, vá até lá andando, evite chegar ao local se empurrando em uma cadeira com rodinhas.

Você também pode complementar este hábito esticando o seu corpo ou fazendo alongamentos. Neste caso, é importante buscar orientação com um profissional no assunto para não executar movimentos inadequados.



