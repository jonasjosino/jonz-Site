<h3> 2 - Deixe os pés retos ao se sentar</h3>

Ao sentar na cadeira, os pés devem ficar completamente no chão, ou seja, não podem ficar inclinados, com somente os dedos tocando a superfície. Caso a cadeira seja muito alta para você, use um apoiador para os pés. Evite jogar os pés para traz, assim como cruzar ou dobrar as pernas enquanto permanecer na cadeira.


